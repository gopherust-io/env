const charset = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"

type field struct {
	name  string
	key   string
	typ   string
	def   string
	value string
}

var (
	smallFields  = smallFieldSpecs()
	mediumFields = buildFields(50, 100)
	largeFields  = buildFields(100, 200)
)

func smallFieldSpecs() []field {
	specs := []struct {
		name  string
		typ   string
		def   string
		value string
	}{
		{"HTTPAddr", "string", ":8080", ":8080"},
		{"NATSURL", "string", "nats://localhost:4222", "nats://localhost:4222"},
		{"NATSCredsFile", "string", "", ""},
		{"NATSToken", "string", "", ""},
		{"MonitoringURL", "string", "http://localhost:8222", "http://localhost:8222"},
		{"RequestTimeout", "time.Duration", "10s", "10s"},
		{"StaticDir", "string", "", ""},
		{"AdminUsername", "string", "admin", "admin"},
		{"AdminPassword", "string", "admin", "admin"},
		{"AuthEnabled", "bool", "true", "true"},
	}
	out := make([]field, len(specs))
	for i, s := range specs {
		out[i] = field{name: s.name, typ: s.typ, def: s.def, value: s.value}
	}
	return out
}

func buildFields(n, seedOffset int) []field {
	out := make([]field, 0, n)
	for i := 0; i < n; i++ {
		def := fmt.Sprintf("v%02d", i)
		typ := "string"
		value := def
		if i >= 10 && i < 20 && n == 50 {
			typ = "int"
			def = fmt.Sprintf("%d", i)
			value = def
		}
		if i >= 20 && i < 30 && n == 50 {
			typ = "bool"
			def = "true"
			value = "true"
			if i%2 == 1 {
				def = "false"
				value = "false"
			}
		}
		if i >= 50 && n == 100 {
			typ = "int"
			def = fmt.Sprintf("%d", i)
			value = def
		}
		if n == 100 {
			def = fmt.Sprintf("v%03d", i)
			value = def
			if i >= 50 {
				typ = "int"
				def = fmt.Sprintf("%d", i)
				value = def
			}
		}
		name := fmt.Sprintf("F%02d", i)
		if n == 100 {
			name = fmt.Sprintf("F%03d", i)
		}
		out = append(out, field{
			name:  name,
			typ:   typ,
			def:   def,
			value: value,
		})
	}
	_ = seedOffset
	return out
}

func assignKeys(fields []field, r *rand.Rand) {
	used := make(map[string]struct{}, len(fields))
	for i := range fields {
		for {
			key := randomKey(r)
			if _, ok := used[key]; ok {
				continue
			}
			used[key] = struct{}{}
			fields[i].key = key
			break
		}
	}
}

func randomKey(r *rand.Rand) string {
	b := make([]byte, 8)
	for i := range b {
		b[i] = charset[r.Intn(len(charset))]
	}
	return string(b)
}

func main() {
	dir := "."
	if len(os.Args) > 1 {
		dir = os.Args[1]
	}

	r := rand.New(rand.NewSource(42))
	assignKeys(smallFields, r)
	assignKeys(mediumFields, rand.New(rand.NewSource(100)))
	assignKeys(largeFields, rand.New(rand.NewSource(200)))

	writeFile(dir, "fixtures_gen.go", writeFixtures)
	writeFile(dir, "competitors_gen.go", writeCompetitors)
	writeFile(dir, "env_setup_gen.go", writeEnvSetup)
	writeFile(dir, "stdlib_load_gen.go", writeStdlibLoaders)
}

func writeFile(dir, name string, fn func(*bytes.Buffer)) {
	var buf bytes.Buffer
	fn(&buf)
	out := filepath.Join(dir, name)
	if err := os.WriteFile(out, buf.Bytes(), 0o644); err != nil {
		panic(err)
	}
	fmt.Println("wrote", out)
}

